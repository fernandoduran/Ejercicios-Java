public class EjemploBuclesAnidados2 {

	public static void main(String[] args) {

		// El programa mostrará diez filas y cada una de ellas
		// tendrá los números del 1 al 50.
		
		for(int i = 1; i<=10; i++){
			
			// Escribimos los números del 1 al 50.
			for(int j = 1; j<=50; j++){
				
				// Escribir el número j
				System.out.print(j + " ");
			}
			
			System.out.println();
		}
		
		
		
		
	}

}
